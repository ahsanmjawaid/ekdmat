        public static string ToBinary(this int intValue) // Converts int integer to Binary string
        {
            // Declare a few variables we're going to need
            Int64 BinaryHolder;
            char[] BinaryArray;
            string BinaryResult = "";

            while (intValue > 0)
            {
                BinaryHolder = intValue % 2;
                BinaryResult += BinaryHolder;
                intValue = intValue / 2;
            }

            // The algorithm gives us the binary number in reverse order (mirrored)
            // We store it in an array so that we can reverse it back to normal
            BinaryArray = BinaryResult.ToCharArray();
            Array.Reverse(BinaryArray);
            BinaryResult = new string(BinaryArray);

            return BinaryResult;
        }
        public static int ToInt(this string bin)// Converts Binary string to integer
        {
            long l = Convert.ToInt64(bin, 2);
            int i = (int)l;
            return i;
        }
        private static byte[] Generate256BitsOfRandomEntropy()
        {
            var randomBytes = new byte[32]; // 32 Bytes will give us 256 bits.
            using (var rngCsp = new RNGCryptoServiceProvider())
            {
                // Fill the array with cryptographically secure random bytes.
                rngCsp.GetBytes(randomBytes);
            }
            return randomBytes;
        }
